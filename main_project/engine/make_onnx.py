from ultralytics import YOLO

# ==========================================
# 1. 원본 파이토치 모델(.pt) 로드
# ==========================================
print("로딩 중: 원본 YOLO 모델 불러오기...")
# 💡 주의: .engine이 아니라 학습이 완료된 원본 .pt 파일 경로를 넣어야 합니다.
# (이전 대화를 참고하여 task는 'classify'로 설정했습니다. 탐지 모델이라면 'detect'로 변경하세요.)
model = YOLO('eye_detector_finetuned_best.pt', task='detect')

# ==========================================
# 2. ONNX 포맷으로 내보내기 (Export)
# ==========================================
print("\n🚀 ONNX 변환을 시작합니다...")

# export() 함수 하나로 모든 변환이 끝납니다.
export_path = model.export(
    format='onnx',      # 🎯 타겟 포맷 지정
    imgsz=416,          # 🎯 입력 해상도 고정 (이전 엔진 크기 128x128 기준)
    half=False,          # 🎯 FP16(반정밀도) 적용 여부 (TensorRT 변환 전 미리 가볍게 만듦)
    simplify=True,      # 🎯 모델 구조 단순화 (노드 병합 등으로 속도 향상, 강력 추천!)
    dynamic=False,      # 🎯 입력 크기(배치/해상도) 고정 (TensorRT 성능을 극한으로 뽑으려면 False 권장)
    device=0            # GPU를 사용하여 변환
)

# ==========================================
# 3. 완료 확인
# ==========================================
print("\n✅ 변환이 완료되었습니다!")
print(f"📁 ONNX 파일 저장 경로: {export_path}")